    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            LoginController register = new LoginController(new User(this.DB, email2.getText(), "user"), namalengkap.getText(), email2.getText(), password2.getText(), nohp.getText());
            if(register.register()){
                session = new User(DB, email2.getText(), "user");
                JOptionPane.showMessageDialog(null,"Pendaftaran User Berhasil!");
                this.dispose();
                
                ViewRead viewRead = new ViewRead(this.DB, session);
                viewRead.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null,"Gagal! Email sudah terdaftar atau Masih ada Form yang kosong!");
            }
        }catch(Exception err){
            System.out.println("errornya ini : " + err);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

