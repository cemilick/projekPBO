    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            try {
                LoginController login = new LoginController(new User(DB),email.getText(), password.getText());
                
                Object[] user = login.login();
                if(user != null){
                    session = new User(DB, user[1].toString(), user[2].toString());
                    this.setVisible(false);
                    if(session.role.equals("admin")){
                        ViewDashboard viewadmin = new ViewDashboard(DB, session);
                        viewadmin.setVisible(true);                        
                    } else {
                        ViewRead viewRead = new ViewRead(this.DB, session);
                        viewRead.setVisible(true);                        
                    }
                    JOptionPane.showMessageDialog(null,"Login Berhasil!");
                } else {
                    JOptionPane.showMessageDialog(null,"Email atau Password Salah!");
                    this.password.setText("");
                }
            } catch (Exception ex) {
                System.out.println(ex);;
            }
        }catch(Exception err){
            System.out.println(err);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

